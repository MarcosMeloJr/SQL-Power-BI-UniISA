    aluno_id INT,
    curso_id INT,
    campus_id INT,
    modalidade_id INT,
    data_matricula DATE,
    PRIMARY KEY (aluno_id, curso_id),
    FOREIGN KEY (curso_id) REFERENCES cursos(curso_id),
    FOREIGN KEY (campus_id) REFERENCES campus(campus_id),
    FOREIGN KEY (modalidade_id) REFERENCES modalidades(modalidade_id)
